#include <xc.inc>
    
;===========================
; Variables en RAM
;===========================
_var:
    PSECT udata_acs
counter:    DS 1
const_ms:   DS 1
ms_count:   DS 1
s_count:    DS 1     

;===========================
; Vector de reset
;===========================
_vector_reset:
    PSECT CODE, RELOC=2, ABS
_reset:
    GOTO _start

;===========================
; Inicio programa (ORG 0x20)
;===========================
ORG 0x20
_start:
    CLRF ADCON0, A
    MOVLW 0x0F
    MOVWF ADCON1, A        ; Todos los pines como digitales
    MOVLW 0x07
    MOVWF CMCON, A         ; Desactiva comparadores
    MOVLW 0xFF
    MOVWF OSCCON, A        ; Oscilador interno 8 MHz

    CLRF counter, A
    CLRF TRISB, A          ; PORTB como salida
    CLRF PORTB, A          ; Limpia PORTB

;===========================
; Bucle principal
;===========================
main:
    MOVF counter, W, A
    MOVWF PORTB, A
    CALL delay_3s
    INCF counter, F, A
    GOTO main

delay_3s:
    MOVLW 3
    MOVWF s_count, A
loop_3s:
    CALL delay_1s
    DECFSZ s_count, F, A
    BRA loop_3s
    RETURN
delay_1s:
    MOVLW 1000
    MOVWF ms_count, A
loop_1s:
    CALL delay_ms        
    DECFSZ ms_count, F, A
    BRA loop_1s
    RETURN
delay_ms:
    MOVLW   498
    MOVWF   const_ms, A
loop_ms:
    NOP
    NOP
    NOP
    NOP
    NOP
    NOP
    NOP
    NOP
    NOP
    NOP
    NOP
    NOP
    NOP
    DECFSZ  const_ms, F, A
    BRA     loop_ms
    RETURN
END
